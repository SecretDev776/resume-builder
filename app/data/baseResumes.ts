export function getProfileByName(name: string | null | undefined): BaseResumeProfile | null {
  if (!name) return null;
  return baseResumes.find((p) => p.name === name) ?? null;
}

export function getBaseResumeByName(name: string | null | undefined): string | null {
  const profile = getProfileByName(name);
  return profile ? profile.resumeText : null;
}
