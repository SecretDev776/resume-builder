export const DEFAULT_THEME_ID = 'professional';

export function getResumeTheme(themeId: string | null | undefined): ResumeTheme {
  if (themeId && resumeThemes[themeId]) {
    return resumeThemes[themeId];
  }
  return resumeThemes[DEFAULT_THEME_ID];
}

export function formatDisplayName(name: string, nameCase: NameCase): string {
  if (!name) return '';
  if (nameCase === 'upper') return name.toUpperCase();
  if (nameCase === 'title') {
    return name.replace(/\S+/g, (word) =>
      word.charAt(0).toUpperCase() + word.slice(1).toLowerCase()
    );
  }
  return name;
}
