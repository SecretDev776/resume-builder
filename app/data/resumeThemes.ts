import { RGB, rgb } from 'pdf-lib';

export type RgbTuple = [number, number, number];

/** Distinct structural layout — reviewers should recognize at a glance. */
export type ResumeTemplate =
  | 'professional' // clean black-and-white, centered header, simple rules
  | 'classic' // soft header card + left stripe + shaded section bars
  | 'modern' // left-aligned, thick stripe, section left-bar, dash bullets
  | 'bold-band' // full-width accent header band, inverted text
  | 'minimal' // sparse rules-only, uppercase sections, monospace feel
  | 'top-stripe' // accent strip on every page + pill section labels
  | 'executive' // serif, centered, double rule, full-width section rules
  | 'sidebar-accent'; // colored vertical bars beside section titles

export type FontFamily = 'helvetica' | 'times' | 'courier';
export type NameCase = 'upper' | 'title' | 'normal';

export type ResumeTheme = {
  id: string;
  label: string;
  template: ResumeTemplate;
  fontFamily: FontFamily;
  nameCase: NameCase;
  bulletChar: string;
  contactSeparator: string;
  heading: RgbTuple;
  body: RgbTuple;
  mediumGray: RgbTuple;
  accent: RgbTuple;
  headerBg: RgbTuple;
  leftRule: RgbTuple;
  /** Text on dark accent surfaces only (header band, section pills). Must contrast with accent. */
  onAccent: RgbTuple;
  headerLayout: 'centered' | 'left';
  showLeftRule: boolean;
  leftRuleThickness: number;
  accentUnderlineThickness: number;
  marginTop: number;
  marginBottom: number;
  marginLeft: number;
  marginRight: number;
  nameSize: number;
  headlineSize: number;
  contactSize: number;
  sectionHeaderSize: number;
  bodySize: number;
  showHeaderCard: boolean;
  showHeaderRule: boolean;
  headerBandHeight: number;
};

export function themeToRgb(tuple: RgbTuple): RGB {
  return rgb(tuple[0], tuple[1], tuple[2]);
}

function themeBase(
  partial: Omit<ResumeTheme, 'id' | 'label'> & { id: string; label: string }
): ResumeTheme {
  return partial;
}

